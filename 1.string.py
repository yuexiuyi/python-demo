message = "hello world"

# title首字母大写
print(message.title())
# upper 全部大写
print(message.upper())
# lower 全部小写
print(message.lower())


first_name = "ada" 
last_name = "lovelace" 
# + 表示字符串拼接
full_name = first_name + " " + last_name

print('Hello' + full_name + '!')


# \n\t表示换行和制表符
content = 'Python\n\tJava '
print(content)

content1 = ' Python Java '

# rstrip 删除字符串末尾的空白
print(content1.rstrip())
# lstrip 删除字符串开头的空白
print(content1.lstrip())
# strip 删除字符串两端的空白
print(content1.strip())


